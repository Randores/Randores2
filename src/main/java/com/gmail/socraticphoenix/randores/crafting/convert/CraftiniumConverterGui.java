package com.gmail.socraticphoenix.randores.crafting.convert;

import com.gmail.socraticphoenix.randores.RandoresKeys;
import net.minecraft.client.gui.inventory.GuiContainer;
import net.minecraft.client.resources.I18n;
import net.minecraft.entity.player.InventoryPlayer;
import net.minecraft.util.math.BlockPos;
import net.minecraft.world.World;

public class CraftiniumConverterGui extends GuiContainer {

    public CraftiniumConverterGui(InventoryPlayer inventoryPlayer, World world, BlockPos converter) {
        super(new CraftiniumConverterContainer(inventoryPlayer, world, converter));
    }

    @Override
    public void drawScreen(int mouseX, int mouseY, float partialTicks) {
        super.drawScreen(mouseX, mouseY, partialTicks);
        this.renderHoveredToolTip(mouseX, mouseY);
    }

    @Override
    protected void drawGuiContainerBackgroundLayer(float partialTicks, int mouseX, int mouseY) {
        //TODO draw the texture
    }

    @Override
    protected void drawGuiContainerForegroundLayer(int mouseX, int mouseY) {
        this.fontRenderer.drawString(I18n.format(RandoresKeys.CRAFTINIUM_CONVERTER), 28, 6, 4210752);
        this.fontRenderer.drawString(I18n.format("container.inventory"), 8, this.ySize - 96 + 2, 4210752);
    }

}
